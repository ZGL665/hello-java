package day4;

public class test1 {

	public static void main(String[] args) {
		
		System.out.println(8);
		System.out.println(010);
		System.out.println(0x11);
		System.out.println(0b1111111);
		//默认int类型，4个字节，不能超过10位
		System.out.println(6653355556L);
		
		System.out.println(3.14159265358f);
		System.out.println(3.141592653589793d);
		
		System.out.println(3.14e2);
		System.out.println(3.14e10);
		
		System.out.println(0.3f+0.6f);
		
		System.out.println('A');
		System.out.println("中");
		
		System.out.println('A'+1);
		System.out.println('\r');
		System.out.println('a'-1);
		System.out.println('\u0061');
		
		System.out.println(0x1);
		
	}

}
